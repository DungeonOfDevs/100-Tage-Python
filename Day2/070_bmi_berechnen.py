# BMI-Rechner

# Der Body-Mass-Index (BMI) ist ein Maß, das in der Medizin verwendet wird, 
# um festzustellen, ob eine Person unter- oder übergewichtig ist.
# Dies ist die Formel zur Berechnung:

# BMI = Gewicht / (Größe²)

###############################################################################

height = 1.65
weight = 84

bmi = weight / height ** 2

print(round(bmi, 2))

