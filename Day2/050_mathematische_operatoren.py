# Mathematische Operationen

# Grundlegende Operatoren:

# Lerne, wie man die grundlegenden mathematischen Operatoren verwendet:
# +, -, *, /, // und **

# Addition
print(123 + 456)    # 579

# Subtraktion
print(7 - 3)        # 4

# Multiplikation
print (3* 2)        # 6

# Division
print(6 / 2)        # 3.0

# Ganzzahlige Division
print(6 // 3)       # 2
print(5 // 3)       # 1

# Potenzen
print(2**2)         # 4
print(2**3)         # 8