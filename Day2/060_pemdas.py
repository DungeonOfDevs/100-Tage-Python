# PEMDAS

# Klammern, Exponenten, Multiplikation/Division, Addition/Subtraktion

# Python rechnet nicht einfach von links nach rechts.

# Reihenfolge:

# P – Parentheses (Klammern)    ()
# E – Exponents (Potenzen)      **
# M – Multiplikation            *
# D – Division                  /
# A – Addition                  +
# S – Subtraktion               -

# ➡ Bei Multiplikation und Division gilt zusätzlich:
# von links nach rechts

# Merksatz:
# PEMDAS (oder PEMDASLR)


# Was ist die Ausgabe dieses Codes?
# print(3 * 3 + 3 / 3 - 3)

print(3 * 3 + 3 / 3 - 3)        # 7.0


# Ändere den Code so, dass er 3 ausgibt.
# print(3 * 3 + 3 / 3 - 3)

print(3 * (3 + 3) / 3 - 3)      # 3.0