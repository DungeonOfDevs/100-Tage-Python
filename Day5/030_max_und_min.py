# Aufgabe 1 – Max():

# Es gibt auch eingebaute Python-Funktionen namens max() und min().
# Diese erlauben es dir, eine Liste von Zahlen zu übergeben, und Python gibt dir:
# - die höchste Zahl (max)
# - oder die niedrigste Zahl (min)
# zurück.

student_scores = [150, 142, 185, 120, 171, 184, 149, 24, 59, 68, 199, 78, 65, 89, 86, 55, 91, 64, 89]

print(max(student_scores))

print(min(student_scores))