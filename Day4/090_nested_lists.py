# Verschachtelte Listen (Nested Lists)

# Du kannst Listen innerhalb anderer Listen speichern. 
# Das nennt man eine verschachtelte Liste („Nested List“) oder auch 2D-Liste.

# Beispiel:

fruits = ["Cherry", "Apple", "Pear"]
veg = ["Cucumber", "Kale", "Spinach"]

fruits_and_veg = [fruits, veg]

print(fruits_and_veg)


# Die Liste sieht dann so aus:

# [
#     ["Cherry", "Apple", "Pear"],
#     ["Cucumber", "Kale", "Spinach"]
# ]


# Du kannst die Liste auch im 2D-Format darstellen, z. B.:

# ["Cherry", "Apple", "Pear"]
# ["Cucumber", "Kale", "Spinach"]